        <h1 class="titcorsi">Maestro Angelo De Paola</h1>
        <p class="storia" id="maestro">Nato a Maida (CZ) nel 1955, il Maestro Angelo De Paola ha studiato Trombone, Composizione e Strumentazione per Banda sotto la guida di illustri maestri napoletani. Laureato presso la Scuola di Paleografia e Filologia della Musica all’ Università di Pavia, è stato docente della classe di Strumentazione per Banda e Direzione di Banda presso il Conservatorio “Stanislao Giacomantonio” di Cosenza per più di 30 anni.<br><br> Alterna la sua attività di ricercatore in campo musicologico a quella di compositore e direttore. Con la BMG Ricordi ha pubblicato un moderno trattato di strumentazione e un libro sulla storia dell’organico per banda (“La Banda. Evoluzione storica dell’organico” pubblicato nel luglio 2002), ai quali, nel dicembre 2005, si è aggiunto un altro di orchestrazione per banda ("Tecnica dell’orchestrazione per i fiati").<br><br> In questo stesso anno lo Stato Maggiore dell'Esercito ha pubblicato un suo lavoro sulle bande militari borboniche.
Nel 2023 ha pubblicato il suo nuovo libro “Aspetti della letteratura per banda nel centro Europa” mettendo in risalto la fondamentale rilevanza della Banda nella cultura e nella letteratura Europea.
È frequentemente invitato come giurato nei vari concorsi musicali per banda e come docente nei corsi di perfezionamento musicale.
