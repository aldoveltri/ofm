        <h1 class="titcorsi">Maestro Giuseppe Gloria</h1>
        <p class="storia" id="maestro">Nato nel 1978 a Cosenza, muove i primi passi musicali sotto la guida del Maestro Tommaso Colloca ed in seguito si iscrive al Conservatorio di Musica "S. Giacomantonio" di Cosenza dove si è diplomato, nel 2002, con ottimi voti in clarinetto, sotto la guida del Maestro Antonio Bifano. Fin da giovane ha svolto un'intensa attività concertistica, occupando il posto di l° clarinetto solista nel coro di clarinetti del conservatorio di Cosenza, nell'Orchestra Lirico Sinfonica e di Fiati del medesimo conservatorio.<br><br>
Ha proseguito gli studi musicali perfezionandosi con il M° Alessandro Carbonare, 1° clarinetto solista dell'Orchestra di Santa Cecila di Roma, e in seguito con il Maestro Giuseppe Garbarino, clarinettista, compositore e direttore d'orchestra. 
Nel campo didattico, dopo aver conseguito la laurea di secondo livello indirizzo didattico, diventa docente di scuola media ad indirizzo musicale. Nel 2005 intraprende gli studi di direzione di banda sotto la guida del Maestro Angelo De Paola e nel 2006, in occasione del “Concerto dell’Epifania”, esordisce alla direzione dell’Orchestra di Fiati Mediterranea.<br><br> Ha frequentato corsi di perfezionamento con Hardy Mertens, Francisco Gnu Vetyara, Lorenzo Parigi, Lino Blanchod. È attualmente il direttore artistico dell'Orchestra di Fiati Mediterranea Città di Amantea, con cui ha partecipato a numerosi concorsi vincendo innumerevoli premi, tra cui il Premio Assoluto in Seconda Categoria al 'Flicorno d'oro' di Riva del Garda. Nel 2009 ha diretto anche l'Orchestra di Fiati del Conservatorio di Cosenza durante i festeggiamenti del 157' anniversario della fondazione delle Polizia di Stato.
